int		ft_strncmp(char *s1, char *s2, unsigned char n)
{
	int		i;

	i = 0;
	while ((s1[i] != '\0') && (s2[i] != '\0') && (n > i))
	{
		if (s1[i] != s2[i])
		{
			return (s1[i] - s2[i]);
		}
		i++;
	}
	if (i == n)
	{
		i--;
	}
	return (s1[i] - s2[i]);
}
