#include "ft.h"

void	ft_strdelspace_p(char *str)
{
	int	i;
	
	i = ft_strlen(str) - 1;
	while (str[i] == ' ' && i >= 0)
	{
		i--;
	}
	str[i + 1] = '\0';
	return ;
}
